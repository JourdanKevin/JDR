    <h1>Qu'est-ce qu'un JDR</h1>
    <p>
        Un jeu de rôle ou abrégé JdR (de l'anglais roleplaying game ; abrégé RPG), aussi appelé jeu de rôle sur table ou
        jeu de rôle papier pour le différencier des autres formes de jeu de rôle, est un jeu de société dans lequel les
        participants conçoivent ensemble une fiction par l’interprétation de rôles et par la narration, dans le cadre de
        contraintes de jeu qu’ils s’imposent.

        Les joueurs de jeu de rôle sont appelés rôlistes, néologisme inventé par Pierre Rosenthal dans les années 19801.
        En 2016, le mot figure sur le dictionnaire Larousse en ligne2.

        Ce jeu, dans sa forme traditionnelle, rassemble les joueurs durant la partie autour d'une table, avec
        communément pour accessoires des dés et feuilles de papier. Ce jeu rassemble aussi des joueurs pour des parties
        sur Internet, et il a inspiré d'autres formes de jeux de rôle tels que le grandeur nature et certains jeux vidéo
    </p>

